    private void atualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atualizarActionPerformed
        
        String nome = textNome.getText().toString().trim();
        String turno = textTurno.getText().toString().trim();
        
        System.out.println(nome + " | " + turno);
        
        Object[] options = {"OK"};
        int n = 0;
        
        if(!nome.isEmpty() && !turno.isEmpty()){
            Graduacao.updateInfos(g, nome, turno);
            n = JOptionPane.showOptionDialog(null,
               "Graduação atualizado com sucesso", "Alerta",
               JOptionPane.PLAIN_MESSAGE,
               JOptionPane.QUESTION_MESSAGE,
               null,
               options,
               options[0]);
            if(n == JOptionPane.OK_OPTION){
               this.setVisible(false);
               new ViewCursosFrame();
               textNome.setText("");
               textTurno.setText("");
            }    
        } else {
            n = JOptionPane.showOptionDialog(null,
               "Preencha os dois campos", "Alerta",
               JOptionPane.PLAIN_MESSAGE,
               JOptionPane.QUESTION_MESSAGE,
               null,
               options,
               options[0]);
        }
    }//GEN-LAST:event_atualizarActionPerformed

