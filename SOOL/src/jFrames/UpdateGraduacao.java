    public boolean checkIfCodigoAlreadyExists(String codigo){
        
        for(Graduacao g : Graduacao.listaGraduacoes){
            if(g.getCodigo().equalsIgnoreCase(codigo)){
                return true;
            }
        }
        
        return false;
        
    }

